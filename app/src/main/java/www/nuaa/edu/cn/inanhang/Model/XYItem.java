public class XYItem {
    private float x;
    private float y;

    public XYItem(float vx, float vy) {
        this.x = vx;
        this.y = vy;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
